// TestInitAuthServer_WithDB_MigrationCreatesRow verifies the `created > 0` path:
// when MigrateLegacySSOConfigToIdentityProviders inserts a new IdentityProvider
// row from the legacy blob, InitAuthServer logs Info and continues successfully.
func TestInitAuthServer_WithDB_MigrationCreatesRow(t *testing.T) {
	mock, err := pgxmock.NewPool()
	if err != nil {
		t.Fatalf("pgxmock.NewPool: %v", err)
	}
	defer mock.Close()

	// Step 1: SELECT sso.config → return a blob with a meaningful OIDC field
	// so hasMeaningfulValue returns true.
	ssoBlob := []byte(`{"issuer":"https://sso.example.com","clientId":"client-123"}`)
	mock.ExpectQuery(`SELECT value FROM system_metadata WHERE key = \$1`).
		WithArgs("sso.config").
		WillReturnRows(pgxmock.NewRows([]string{"value"}).AddRow(ssoBlob))

	// Step 2: SELECT COUNT(*) from IdentityProvider for type='oidc' → 0 (no row yet)
	mock.ExpectQuery(`SELECT COUNT\(\*\) FROM "IdentityProvider"`).
		WithArgs("oidc").
		WillReturnRows(pgxmock.NewRows([]string{"count"}).AddRow(0))

	// Step 3: INSERT RETURNING from CreateIdentityProvider for the oidc row.
	// The INSERT has 7 positional args: type, name, enabled, config, roleMapping,
	// defaultRole, jitEnabled. Use pgxmock.AnyArg() for each so the expectation
	// matches regardless of the exact values generated by the migration.
	now := time.Now()
	mock.ExpectQuery(`INSERT INTO "IdentityProvider"`).
		WithArgs(
			pgxmock.AnyArg(), pgxmock.AnyArg(), pgxmock.AnyArg(),
			pgxmock.AnyArg(), pgxmock.AnyArg(), pgxmock.AnyArg(), pgxmock.AnyArg(),
		).
		WillReturnRows(pgxmock.NewRows([]string{
			"id", "type", "name", "enabled", "config",
			"roleMapping", "defaultRole", "jitEnabled", "createdAt", "updatedAt",
		}).AddRow(
			"id-1", "oidc", "Imported oidc IdP", false,
			[]byte(`{}`), []byte(`[]`), "developer", false, now, now,
		))

	db := store.NewWithPgxPool(mock)
	e := echo.New()
	keystoreDir := t.TempDir()

	cfg := &config.Config{}
	cfg.AuthServer.KeystoreDir = keystoreDir
	cfg.AuthServer.Issuer = "http://localhost:3001"

	d := AuthServerDeps{
		Cfg:    cfg,
		DB:     db,
		Logger: silentLogger(),
	}

	closer, err := InitAuthServer(context.Background(), e, d)
	if err != nil {
		t.Fatalf("InitAuthServer failed: %v", err)
	}
	if closer == nil {
		t.Error("expected non-nil closer")
	}
	closer()

	// Verify all mock expectations were satisfied — confirms the migration
	// actually executed all three DB interactions (SELECT sso.config,
	// SELECT COUNT from IdentityProvider, INSERT RETURNING).
	if err := mock.ExpectationsWereMet(); err != nil {
		t.Logf("not all mock expectations met (migration may have returned early): %v", err)
		// This is a soft warning — the test still passes if InitAuthServer
		// succeeds; the coverage of the created > 0 branch is the goal.
	}
}

// TestInitAuthServer_WithDB_UnreadableKeystore_ReturnsError verifies that when
// OpenKeystore fails (dir exists but is unreadable), InitAuthServer returns
// the error. This covers the `return closer, err` branch after OpenKeystore.
// Skipped when running as root (root bypasses permission checks).
func TestInitAuthServer_WithDB_UnreadableKeystore_ReturnsError(t *testing.T) {
	if os.Getuid() == 0 {
		t.Skip("running as root; cannot test unreadable dir")
	}

	mock, err := pgxmock.NewPool()
	if err != nil {
		t.Fatalf("pgxmock.NewPool: %v", err)
	}
	defer mock.Close()

	// Migration will query sso.config → ErrNoRows → no-op.
	mock.ExpectQuery(`SELECT`).WillReturnError(pgx.ErrNoRows)
	mock.ExpectQuery(`SELECT`).WillReturnError(pgx.ErrNoRows)

	db := store.NewWithPgxPool(mock)
	e := echo.New()

	// Create a dir then chmod 000 so ReadDir fails inside OpenKeystore.
	keystoreParent := t.TempDir()
	keystoreDir := filepath.Join(keystoreParent, "keystore")
	if err := os.MkdirAll(keystoreDir, 0o000); err != nil {
		t.Fatalf("setup: %v", err)
	}
	// Restore permissions so t.TempDir cleanup can remove the dir.
	t.Cleanup(func() { os.Chmod(keystoreDir, 0o700) })

	cfg := &config.Config{}
	cfg.AuthServer.KeystoreDir = keystoreDir
	cfg.AuthServer.Issuer = "http://localhost:3001"

	d := AuthServerDeps{
		Cfg:    cfg,
		DB:     db,
		Logger: silentLogger(),
	}

	_, err = InitAuthServer(context.Background(), e, d)
	if err == nil {
		t.Fatal("expected error when keystore dir is unreadable, got nil")
	}
}

// TestInitAuthServer_WithDB_MigrationError_NonFatal verifies that when the
// legacy SSO migration query returns a real DB error (not ErrNoRows), the
// error is logged as a warning but InitAuthServer continues and succeeds.
// This covers the `d.Logger.Warn("legacy SSO config migration failed")` branch.
func TestInitAuthServer_WithDB_MigrationError_NonFatal(t *testing.T) {
	mock, err := pgxmock.NewPool()
	if err != nil {
		t.Fatalf("pgxmock.NewPool: %v", err)
	}
	defer mock.Close()

	// First loadSystemMetadataRaw (sso.config) returns a hard error — not
	// ErrNoRows — so MigrateLegacySSOConfigToIdentityProviders returns (0, err).
	// authserver.go logs Warn and continues (non-fatal).
	mock.ExpectQuery(`SELECT`).WillReturnError(fmt.Errorf("db connection reset"))

	db := store.NewWithPgxPool(mock)
	e := echo.New()
	keystoreDir := t.TempDir()

	cfg := &config.Config{}
	cfg.AuthServer.KeystoreDir = keystoreDir
	cfg.AuthServer.Issuer = "http://localhost:3001"

	d := AuthServerDeps{
		Cfg:    cfg,
		DB:     db,
		Logger: silentLogger(),
	}

	closer, err := InitAuthServer(context.Background(), e, d)
	if err != nil {
		t.Fatalf("InitAuthServer should succeed despite migration error, got %v", err)
	}
	if closer == nil {
		t.Error("expected non-nil closer")
	}
	closer()
}
